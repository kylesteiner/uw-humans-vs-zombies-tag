A. Overview<br />
<br />
Humans vs. Zombies Tag is a zombie apocalypse-themed game of tag. There are two teams: the Human Resistance, and the Zombie Horde. The Humans attempt to outlast the Zombies until the Humans can escape to safety or all of the Zombies starve to death. The Zombies strive to infect all of the Humans, turning them into Zombies.<br />
<br />
B. Eligibility and Play time<br />
<br />
Any registered UW student or faculty member may participate. Individuals who would like to participate, but are neither faculty, nor students, may be granted an exception by speaking with the Overseers. Decisions will be made by the Overseers on a case-by-case basis.<br />
<br />
Anyone not participating is prohibited from directly intervening in the game. This includes bringing food to Humans or spying for either team.<br />
<br />
The game begins at 6:00am on Monday morning, and continues until dusk, which will be assessed before the start of each game. For the Winter Quarter, 2013 game, play will be paused after 6:00pm or after the completion of the mission. Play will be paused immediately following the mission, no matter what time the mission ends. Play will resume the next day at 6:00am.<br />
<br />
C. Supplies<br />
<br />
Orange bandana, purchased at registration. A bandana costs $2, and may be used for multiple quarters of play.<br />
<br />
Index card ID, created at registration. Each card displays the owner&rsquo;s full name, phone number, e-mail address, and a random six-digit serial number used to authenticate tag reports. Humans must keep their card on them at all times and surrender it to a Zombie who tags them. See &ldquo;Zombies tagging Humans&rdquo; for more information.<br />
<br />
Optional: Nerf blaster(s), rolled sock(s). See &ldquo;Humans stunning Zombies&rdquo; and &ldquo;Weaponry&rdquo;.<br />
<br />
D. Bandanas and Placement<br />
<br />
Orange (and in a few instances, green) bandanas serve to identify game players and must be worn at all times while within free-play areas. Their placement on the body indicates whether players are Humans or Zombies.<br />
<br />
Humans wear their bandana around their left or right upper-arm. Zombies, when active, wear their bandana around their head only. When stunned, Zombies wear their bandana around the neck. In all cases, bandanas should be plainly visible and never obscured by hats, hoods, umbrellas, or anything else of the sort.<br />
<br />
Bandanas may be removed while in safe zones, but must be put on completely before entering a free-play area. Any tags or stuns made without a bandana are invalid.<br />
<br />
Players in ROTC and similar organizations that require professional dress and behavior at certain times are exempt from wearing their bandana during those times.<br />
<br />
E. Original Zombies<br />
<br />
The rise of the Zombie Horde begins with the introduction of a number of Original Zombies, a special class of Zombie that is permitted to operate without a bandana for the first 24 hours of the game. Original Zombies obey the same tagging and stunning rules as regular Zombies, but with the added benefit of stealth.<br />
<br />
After the first day has passed, the Original Zombies must wear their bandanas again.<br />
<br />
F. Humans Stunning Zombies<br />
<br />
Aside from simply running away, Humans can defend themselves against Zombies by stunning their attackers. Zombies are stunned if struck by a foam dart fired from a blaster or by a rolled sock thrown by a Human. See &ldquo;Weaponry&rdquo; for more information on valid equipment and modifications.<br />
<br />
Stunned Zombies are effectively out of the game for 15 minutes. They cannot tag Humans, stalk them, engage them in conversation, or in any way assist other Zombies during the stun.<br />
<br />
As soon as Zombies are stunned, they must immediately move their bandana from around their head to around their neck. After 15 minutes has passed, stunned Zombies must move inside of a building before they can return their bandana to their head and resume hunting.<br />
<br />
In the event that the stunned Zombie is part of a larger skirmish, he or she must immediately move out of the line of fire so as not to distract other combatants and to avoid acting as a &ldquo;meat shield&rdquo; for active Zombies.<br />
<br />
G. Zombies Tagging Humans<br />
<br />
Zombies infect Humans by tagging them. A tag is defined as a firm touch on any part of the body. A firm touch should be recognizable by both parties; merely brushing the victim is insufficient. Anything you are carrying is considered an extension of your body.<br />
<br />
Tackles and other applications of excessive force are never acceptable means of completing a tag, and any tag made with such is invalid. Offenders are subject to disqualification from the game.<br />
<br />
When a Zombie successfully tags a Human, the Human must immediately surrender his or her index card ID to the Zombie. The Zombie must report their tag at UWHvZT.com. Tags should be reported the same day.<br />
<br />
Humans who have been tagged become Zombies 15 minutes after they are tagged. Prior to that time, tagged Humans are no longer a member of either team and must remove their bandana, and put away their blaster(s), during the transition. If the blaster cannot be put away within the 15 minute change time, the tagged Human may not participate as a Zombie until the blaster is sufficiently put away.<br />
<br />
Zombies must tag a Human every 48 hours to remain in the game. Zombies who go without a tag for more than 48 hours &ldquo;starve&rdquo; and are out of the game. Any zombie who attends the Wednesday mission cannot starve.<br />
<br />
H. Weaponry and Attire<br />
<br />
All projectile-launching equipment must be either Nerf or Buzz Bee brand; no homemade blasters, please. Modifications are permitted as long as the owner is comfortable shooting him or herself in the face repeatedly at point-blank range. Wielding a particularly painful blaster does not make you badass; it marks you for disqualification from the game.<br />
<br />
Do not paint your blaster. Do not paint your blaster. DO NOT PAINT YOUR BLASTER.<br />
<br />
Aftermarket attachments to blasters are also not allowed. This includes, but is not limited to, flashlights, strobe lights, and laser sights. Only blaster attachments produced by a blaster manufacturer (ex. Nerf, Buzz Bee) are allowed.<br />
<br />
Socks should be clean and rolled up only, not cut or stretched. Modify socks with more socks and nothing else.<br />
<br />
Melee weapons of any sort are absolutely prohibited, and their use constitutes an immediate disqualification from the current game and banishment from all future games.<br />
<br />
No shields. Anything you are carrying is considered an extension of your body.<br />
<br />
I. Super Zombies<br />
<br />
Zombies who tag 10 or more Humans are eligible to become Super Zombies. That Zombie should present the 10 index cards taken from the humans they turned to the Overseers to receive a green bandanna. Super Zombies are distinguishable from ordinary Zombies by the green bandana, which is worn around their head. These super zombies are then invulnerable to being stunned by socks or darts. Once a super zombie has tagged a human, they will be stunned for 15 minutes.
<br />
J. Safe Zones and Free-play Areas<br />
<br />
Humans and Zombies cannot be tagged or stunned while within safe zones. Safe zones include all indoor areas (dorms, academic buildings, businesses, etc.), official athletic practices and facilities (Husky Stadium, IMA), outdoor academic events, buses in certain conditions (see section &ldquo;Buses&rdquo;), and within the boundaries of an automobile (including truck beds).<br />
<br />
Free-play areas include all outdoor spaces that are not specifically closed to the general public. (except those indicated above).<br />
<br />
While anywhere off-campus (other than the exceptions listed in this section) is technically considered a free-play area, please exercise common sense when planning an attack. The law always supersedes these rules.<br />
<br />
Players cannot stun or tag across safe zone boundaries, even if opponents across the boundary are within reach.<br />
<br />
To be considered safe, a play must be touching a door that is able to be opened.<br />
<br />
Players are absolutely prohibited from holding doors closed, barricading doors, or otherwise physically impeding passage. At the other extreme, players may not force another player to leave a safe area against his or her will. This includes picking up and carrying that player outside.<br />
<br />
No-play areas include the Red Square parking garage, and the Padelford parking garage, the Allen Library loading dock, and all of the University Village Shopping Center. Parking garages may only be entered by a player that is parking a car in the garage, or by a player leaving campus via their car in the garage. Any player caught actively participating in the game while in a no-play zone will be subject to discipline.<br />
<br />
K. Buses<br />
<br />
The area within 15 feet of buses is considered a safe zone given that, (1) the bus is at a bus stop, (2) the bus is stationary, and (3) you are going to board that bus. If not all three criteria are met, then proximity to the bus does not grant you safety.<br />
<br />
L. Vehicles<br />
<br />
Non-automotive vehicle use is prohibited during the game; no scooters, long boards, skateboards, rollerblades, Heelys, etc. Bicycles have a limited exception to this ban as detailed below.<br />
<br />
While on campus proper, defined here as the area within the boundaries of NE 45th St, 15th Ave NE, and the Burke-Gilman Trail, bicycles must not be used. Upon entering this area, bicyclists should seek to dismount and secure their bikes as soon as possible, as riding a bike does not confer any invulnerability within the area. However, players should not engage in attacks on player cyclists that jeopardize the safety of themselves or the cyclists.<br />
<br />
Outside of the area defined above, bicyclists are immune to stuns or tags while the bike is in motion and both of the bicyclist&rsquo;s feet are not in contact with the ground. If the bike is stationary, or if the bicyclist is touching the ground, the bicyclist is vulnerable to attack. Bicyclists must be fully dismounted before they can stun or tag.<br />
<br />
Cars are allowed when used enter campus, however once a player has reached campus the car may be visited, but may not be used to travel except to leave campus.<br />
<br />
M. Missions<br />
<br />
The game will end after the mission has ended on nights without missions the game will end at a time that varies between quarters to be set in that game's rules.<br />
<br />
A typical game of HvZT features an optional Wednesday mission and a mandatory final Friday mission. Missions serve to promote inter- and intra-team interactions, as well as provide rewards for completion of various objectives. The game is not limited to only two missions.<br />
<br />
Mission details are distributed beforehand via the UWHvZT website (http://www.uwhvzt.com).<br />
<br />
Rewards for success and/or penalties for failure may change based upon each team&rsquo;s performance during missions, so players are encouraged to go out and lend their assistance.<br />
<br />
N. Victory Conditions<br />
<br />
Humans who successfully complete the final mission escape the clutches of the Zombie Horde and are officially recognized as survivors of the game. Humans who fail to complete the final mission are assumed to be overwhelmed by the Zombie Horde, and subsequently infected.<br />
<br />
The Zombie Horde is successful if no Humans complete the final mission.<br />
<br />
O. Disputes and Arbitration<br />
<br />
In the event of a disagreement concerning stuns, tags, safe zones, and so forth, the involved parties should first attempt to reach a resolution themselves. If an agreement cannot be reached, all parties should e-mail the UW HvZT Overseers at uwhvzt@uw.edu to request arbitration. Include the names of those involved and a detailed description of the incident. Remain civil and courteous at all times.<br />
<br />
P. Player Conduct<br />
<br />
Players should not enter any area that is specifically closed to the general public. Areas that are roped off for landscaping, or closed for construction are not to be entered. Any player caught entering areas that are specifically closed will be subject to discipline.<br />
<br />
Be courteous with your weaponry. Avoid aiming for an opponent&rsquo;s head; it&rsquo;s rude and unnecessary. Don&rsquo;t continue shooting a Zombie after he or she has already been stunned. Don&rsquo;t modify your blaster so much that it hurts other players. Players are responsible for keeping track of their darts and socks, as well as cleaning up after themselves. Littering is not appreciated.<br />
<br />
Players should be especially careful to avoid striking non-players with projectiles, or colliding with non-players. Angry bystanders make for poor public relations, and the game can be cancelled if collateral damage becomes an issue. Any player found to have struck a bystander will be subject to immediate disqualification from the game.<br />
<br />
While players are allowed to keep their weapons on themselves in class, the libraries, and other safe zones, they should never flaunt them, point them at people, or be evasive about them. Many previous incidents have occurred in Smith Hall and Allen Library. Extra care should be taken care if entering these buildings. All inquiries should be answered truthfully. While weaponry needn&rsquo;t be concealed, it should be sufficiently put away while in safe zones.<br />
<br />
Players are to present themselves in a professional and campus appropriate manner at all times. Players should not interrupt the normal proceedings of the University of Washington. Players should not run or yell while indoors. Players should not interrupt classes or other events occurring at the same time as the game.<br />
<br />
While players are encouraged to actively participate in the game, and be enthusiastic about it, this is not a reason to go overboard. Masks, combat attire (this includes but is not limited to, camouflage of any kind, combat fatigues, and flak or &quot;tactical&quot; vests), and other unnecessary equipment are not to be used, and could result in disqualification from the game.<br />
<br />
Players are to respect staff requests. If a staff member asks for a player&rsquo;s name, or requests the players to leave due to distractions, the player must provide the information, and cooperate to the best of their ability. This is to ensure the longevity of the game on campus.<br />
<br />
The law supersedes gameplay at all times. Any player caught jaywalking, or breaking any other law, will be immediately removed from the game and referred to the UWPD.<br />
<br />
The Overseers keep a database of all players, and the status of all players during the game. Your personal information will only be used by an Overseer to contact you if you have been involved in a major incident.<br />
